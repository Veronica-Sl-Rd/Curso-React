export const getProductos = () => {
    let error = false
    return new Promise ((resolve, reject)=>{
        setTimeout(()=> {
            if (error){
                reject('Ups, algo salió mal')
            } else {
                resolve(productos)
            }
        })
    })
}

export const getOneProduct = (id) => {
    return new Promise ((resolve)=> {
        setTimeout(()=> {
            let prod= productos.find((producto)=> producto.id === id)
            resolve(prod)
        }, 2000)
    })
}